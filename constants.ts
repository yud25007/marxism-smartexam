const letterToIndex = (letter: string) => letter.charCodeAt(0) - 65;

// Helper to extract options from {"A": "...", "B": "..."} object to array
const getOptionsArray = (optionsObj: Record<string, string>): string[] => {
  const keys = ['A', 'B', 'C', 'D', 'E', 'F'];
  const result: string[] = [];
  for (const k of keys) {
    if (optionsObj[k]) {
      result.push(`${k}. ${optionsObj[k]}`); // Keep the letter prefix for clarity
    }
  }
  return result;
};

// Transform raw data into App's Exam structure
export const EXAMS: Exam[] = RAW_DATA.chapters.map((chapter) => {
  const questions: Question[] = [];

  // Process Single Choice
  chapter.single_choice.forEach((q: any) => {
    questions.push({
      id: `ch${chapter.id}_sc_${q.id}`,
      type: QuestionType.SINGLE_CHOICE,
      text: q.question,
      options: getOptionsArray(q.options),
      correctAnswers: [letterToIndex(q.answer)],
      points: 2
    });
  });

  // Process Multiple Choice
  chapter.multiple_choice.forEach((q: any) => {
    questions.push({
      id: `ch${chapter.id}_mc_${q.id}`,
      type: QuestionType.MULTIPLE_CHOICE,
      text: q.question,
      options: getOptionsArray(q.options),
      correctAnswers: q.answer.map((a: string) => letterToIndex(a)).sort((a: number, b: number) => a - b),
      points: 4
    });
  });

  // Process True/False
  chapter.true_false.forEach((q: any) => {
    questions.push({
      id: `ch${chapter.id}_tf_${q.id}`,
      type: QuestionType.TRUE_FALSE,
      text: q.question,
      options: ['正确', '错误'],
      // Assuming true = Correct(0), false = Incorrect(1) based on typical true/false layouts
      correctAnswers: [q.answer === true ? 0 : 1], 
      points: 2
    });
  });

  // Process Short Answer
  if (chapter.short_answer) {
    chapter.short_answer.forEach((q: any) => {
      questions.push({
        id: `ch${chapter.id}_sa_${q.id}`,
        type: QuestionType.SHORT_ANSWER,
        text: q.question,
        options: [],
        correctAnswers: [],
        answerText: q.answer,
        points: 5
      });
    });
  }

  // Calculate difficulty/stats
  const totalQuestions = questions.length;
  // Simple estimation for duration
  const saCount = chapter.short_answer ? chapter.short_answer.length : 0;
  const duration = Math.ceil((chapter.single_choice.length * 1 + chapter.true_false.length * 1 + chapter.multiple_choice.length * 2 + saCount * 3)); 

  return {
    id: `chapter_${chapter.id}`,
    title: chapter.name,
    description: `本章节包含单选题、多选题和判断题共 ${totalQuestions} 道。`,
    category: '马克思主义基本原理',
    durationMinutes: duration > 0 ? duration : 30, // Default to 30 if calc is 0
    questionCount: totalQuestions,
    difficulty: 'Medium', // Defaulting to medium
    coverImage: `https://picsum.photos/seed/${chapter.id + 100}/800/600`,
    questions: questions
  };
});